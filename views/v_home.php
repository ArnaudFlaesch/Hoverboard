    <h4>Qu'est-ce que le projet Hoverboard ?</h4>
    </br>
    </br>
    Hoverboard est une application réalisée dans le cadre du projet annuel de troisième année d'Architecture des Logiciels </br>
    de l'école ESGI (École Supérieure du Génie Informatique). Elle permettra à ses utilisateurs de créer des panneaux sur lesquels </br>
    seront stockées des informations écrites sur des post-its, des listes de tâches ou encore des sondages auxquels d'autres utilisateurs </br>
    pourront répondre. </br>
    
    </br>
    </br>
    
